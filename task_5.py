class 학생:
    def __init__(self, 이름, 국어, 영어, 수학):
        self.이름 = 이름
        self.국어 = 국어
        self.영어 = 영어
        self.수학 = 수학

def 평균_점수(학생들):
    총합_국어 = 0
    총합_영어 = 0
    총합_수학 = 0

    for 학생 in 학생들:
        총합_국어 += 학생.국어
        총합_영어 += 학생.영어
        총합_수학 += 학생.수학

    평균_국어 = 총합_국어 / len(학생들)
    평균_영어 = 총합_영어 / len(학생들)
    평균_수학 = 총합_수학 / len(학생들)

    return 평균_국어, 평균_영어, 평균_수학

def main():
    학생들 = []

    학생수 = int(input("학생 수를 입력하세요: "))

    for i in range(학생수):
        이름 = input(f"{i+1}번째 학생의 이름을 입력하세요: ")
        국어 = int(input(f"{이름} 학생의 국어 점수를 입력하세요: "))
        영어 = int(input(f"{이름} 학생의 영어 점수를 입력하세요: "))
        수학 = int(input(f"{이름} 학생의 수학 점수를 입력하세요: "))

        학생들.append(학생(이름, 국어, 영어, 수학))

    평균_국어, 평균_영어, 평균_수학 = 평균_점수(학생들)

    print("\n전체 학생들의 평균 점수는 다음과 같습니다:")
    print(f"국어 평균 점수: {평균_국어}")
    print(f"영어 평균 점수: {평균_영어}")
    print(f"수학 평균 점수: {평균_수학}")

if __name__ == "__main__":
    main()
